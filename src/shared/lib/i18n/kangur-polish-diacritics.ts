const escapeRegExp = (value: string): string => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

const toTitleCase = (value: string): string =>
  value.length > 0
    ? `${value[0]?.toLocaleUpperCase('pl-PL')}${value.slice(1)}`
    : value;

const createUnicodeWordPattern = (value: string): RegExp =>
  new RegExp(`(?<![\\p{L}\\p{N}_])${escapeRegExp(value)}(?![\\p{L}\\p{N}_])`, 'gu');

const wordReplacements = baseWordReplacements.flatMap(([from, to]) => [
  [createUnicodeWordPattern(from), to] as const,
  [createUnicodeWordPattern(toTitleCase(from)), toTitleCase(to)] as const,
]);

const isPlainObject = (value: unknown): value is Record<string, unknown> => {
  if (typeof value !== 'object' || value === null) {
    return false;
  }

  const prototype = Reflect.getPrototypeOf(value);
  return prototype === Object.prototype || prototype === null;
};

export const repairKangurPolishText = (value: string): string => {
  let next = value;

  for (const [from, to] of exactPhraseReplacements) {
    next = next.replaceAll(from, to);
  }

  for (const [pattern, replacement] of wordReplacements) {
    next = next.replace(pattern, replacement);
  }

  return next;
};

const repairKangurPolishCopyValue = (value: unknown): unknown => {
  if (typeof value === 'string') {
    return repairKangurPolishText(value);
  }

  if (Array.isArray(value)) {
    return value.map((entry) => repairKangurPolishCopyValue(entry));
  }

  if (value instanceof Date) {
    return value;
  }

  if (
    typeof value === 'object' &&
    value !== null &&
    '_bsontype' in (value as Record<string, unknown>)
  ) {
    return value;
  }

  if (!isPlainObject(value)) {
    return value;
  }

  return Object.fromEntries(
    Object.entries(value).map(([key, entry]) => [key, repairKangurPolishCopyValue(entry)])
  );
};

export const repairKangurPolishCopy = <T>(value: T): T => {
  return repairKangurPolishCopyValue(value) as T;
};
